            <div class="preamble-section">
              <h3>
                <span class="english-text">Procedure for Issuance of No Objection Certificate (NOC) for International Funds by the Indian International Economic Service (IIES)</span>
                <span class="hindi-text">भारतीय अंतर्राष्ट्रीय आर्थिक सेवा (IIES) द्वारा अंतर्राष्ट्रीय धन के लिए अनापत्ति प्रमाणपत्र (NOC) जारी करने की प्रक्रिया</span>
              </h3>
              <p class="english-text">
                As per the directives of the IIES, Government of India, the Indian International Economic Service (IIES) has been authorized to issue a No Objection Certificate (NOC) to both Indian citizens and Persons of Indian Origin (PIOs) in respect of their international or foreign funds. This certificate serves as an official document certifying that there are no financial, legal, or regulatory objections from the Government of India or any financial authority regarding the said international amount, and that all statutory formalities have been duly completed for its withdrawal or utilization.
              </p>
              <p class="hindi-text">
                भारत सरकार के IIES के निर्देशानुसार, भारतीय अंतर्राष्ट्रीय आर्थिक सेवा (IIES) को भारतीय नागरिकों तथा भारतीय मूल के व्यक्तियों (PIOs) के अंतर्राष्ट्रीय/विदेशी धन के संबंध में अनापत्ति प्रमाणपत्र (NOC) जारी करने का अधिकार प्राप्त है। यह प्रमाणपत्र इस बात का आधिकारिक प्रमाण है कि संबंधित राशि पर भारत सरकार या किसी वित्तीय प्राधिकरण की ओर से कोई वित्तीय, कानूनी या नियामकीय आपत्ति नहीं है और निकासी/उपयोग हेतु सभी वैधानिक औपचारिकताएँ पूर्ण हो चुकी हैं।
              </p>
            </div>

           

            <!-- Initial Application Procedure for International Funds -->
            <div class="role-section">
              <h3>
                <span class="english-text">Initial Application Procedure for International Funds</span>
                <span class="hindi-text">अंतर्राष्ट्रीय धन हेतु प्रारंभिक आवेदन प्रक्रिया</span>
              </h3>
              <p class="english-text">For any form of international fund — whether received as investment, donation, grant, foreign aid, or inheritance — the owner or recipient of the amount must first obtain a Payment Ledger Book from the Indian International Finance Commission (IIFC).</p>
              <p class="hindi-text">अंतर्राष्ट्रीय धन के किसी भी रूप (निवेश, दान, अनुदान, विदेशी सहायता या विरासत) के लिए, राशि के स्वामी/प्राप्तकर्ता को पहले भारतीय अंतर्राष्ट्रीय वित्त आयोग (IIFC) से भुगतान लेखा बही प्राप्त करनी होगी।</p>
              <p class="english-text">This ledger must include full details of the received international funds, such as:</p>
              <p class="hindi-text">इस लेखा बही में प्राप्त अंतर्राष्ट्रीय धन का विस्तृत विवरण शामिल होना चाहिए, जैसे:</p>
              <ol class="numbered-list">
                <li><span class="english-text">The total amount received,</span><span class="hindi-text">प्राप्त कुल राशि,</span></li>
                <li><span class="english-text">Name of the source country,</span><span class="hindi-text">मूल स्रोत देश का नाम,</span></li>
                <li><span class="english-text">Transaction ID,</span><span class="hindi-text">लेनदेन आईडी,</span></li>
                <li><span class="english-text">Date of receipt,</span><span class="hindi-text">प्राप्ति की तिथि,</span></li>
                <li><span class="english-text">Beneficiary’s banking details. Purpose or nature of the fund,</span><span class="hindi-text">लाभार्थी के बैंक विवरण; धन का उद्देश्य/स्वरूप,</span></li>
              </ol>
            </div>

            <div class="role-section role-senior">
              <h3>
                <span class="english-text">International Revenue Accounting and Certification</span>
                <span class="hindi-text">अंतर्राष्ट्रीय राजस्व लेखांकन एवं प्रमाणीकरण</span>
              </h3>
              <p class="english-text">In all cases of international monetary transactions, accounting and verification of the received funds shall be conducted by the National Financial Reporting Authority (NFRA) — a statutory body under the Government of India.</p>
              <p class="hindi-text">अंतर्राष्ट्रीय धन लेनदेन के सभी मामलों में प्राप्त धन का लेखांकन एवं सत्यापन राष्ट्रीय वित्तीय प्रतिवेदन प्राधिकरण (NFRA) द्वारा किया जाएगा — जो भारत सरकार के अधीन एक सांविधिक निकाय है।</p>
              <p class="english-text">The NFRA ensures that all foreign funds received within India comply with the provisions of the Foreign Contribution Regulation Act (FCRA) and the established Indian financial transparency standards.</p>
              <p class="hindi-text">NFRA यह सुनिश्चित करता है कि भारत में प्राप्त सभी विदेशी धन विदेशी अंशदान विनियमन अधिनियम (FCRA) और स्थापित भारतीय वित्तीय पारदर्शिता मानकों का पालन करें।</p>
              <p class="english-text">The NFRA examines whether the source of funds is legitimate and that the purpose of utilization aligns with India’s lawful economic and social interests. Upon successful verification, the applicant is issued a Certificate of International Revenue Accounting and Financial Compliance by the NFRA.</p>
              <p class="hindi-text">NFRA निधि के स्रोत की वैधता तथा उपयोग के उद्देश्य की भारत के वैध आर्थिक‑सामाजिक हितों से सामंजस्य की जाँच करता है। सफल सत्यापन के उपरांत आवेदक को NFRA द्वारा अंतर्राष्ट्रीय राजस्व लेखांकन एवं वित्तीय अनुपालन प्रमाणपत्र जारी किया जाता है।</p>
            </div>

            <div class="role-section role-mid">
              <h3>
                <span class="english-text">Obtaining the Form-28 Certificate from the International Finance Commission</span>
                <span class="hindi-text">अंतर्राष्ट्रीय वित्त आयोग से फॉर्म‑28 प्रमाणपत्र प्राप्त करना</span>
              </h3>
             <p class="english-text">After receiving certification from the NFRA, the applicant must apply to the Indian International Finance Commission (IIFC) for issuance of the Form-28 Certificate. This certificate officially confirms that the international funds have been duly approved under the financial guidelines of the Government of India and that there are no existing legal or administrative restrictions on their use or transfer.</p>
             <p class="hindi-text">NFRA से प्रमाणन प्राप्त करने के पश्चात्, आवेदक को भारतीय अंतर्राष्ट्रीय वित्त आयोग (IIFC) में फॉर्म‑28 प्रमाणपत्र जारी करने हेतु आवेदन करना होगा। यह प्रमाणपत्र औपचारिक रूप से पुष्टि करता है कि अंतर्राष्ट्रीय धन भारत सरकार के वित्तीय दिशानिर्देशों के अंतर्गत विधिवत स्वीकृत है और उसके उपयोग/हस्तांतरण पर कोई विधिक या प्रशासनिक प्रतिबंध शेष नहीं है।</p>
            </div>

            <div class="role-section role-junior">
              <h3>
                <span class="english-text">Payment and Regulation of NOC Fees</span>
                <span class="hindi-text">NOC शुल्क का भुगतान एवं विनियमन</span>
              </h3>
              <p class="english-text">Once the Form-28 Certificate has been obtained, the applicant is required to pay the No Objection Certificate (NOC) Fee. This fee is determined annually based on the total value and category of the international funds (such as donation, grant, investment, etc.). The rate of NOC fee is revised periodically as per notifications published in the Gazette of the IIES. The fee can be paid through the official portal of the Indian International Economic Service or directly via the IIFC Treasury Department.</p>
              <p class="hindi-text">फॉर्म‑28 प्रमाणपत्र प्राप्त होने के बाद, आवेदक को अनापत्ति प्रमाणपत्र (NOC) शुल्क का भुगतान करना आवश्यक है। यह शुल्क अंतर्राष्ट्रीय धन के कुल मूल्य और श्रेणी (जैसे दान, अनुदान, निवेश आदि) के आधार पर वार्षिक रूप से निर्धारित होता है तथा IIES के राजपत्र में प्रकाशित अधिसूचनाओं के अनुसार समय‑समय पर संशोधित किया जाता है। शुल्क का भुगतान IIES के आधिकारिक पोर्टल या सीधे IIFC कोषागार विभाग के माध्यम से किया जा सकता है।</p>
            </div>

              <div class="role-section role-junior">
              <h3>
                <span class="english-text">Issuance of the Final No Objection Certificate (Form 28-B)</span>
                <span class="hindi-text">अंतिम अनापत्ति प्रमाणपत्र (फॉर्म 28‑B) का निर्गमन</span>
              </h3>
              <p class="english-text">Upon successful payment of the NOC fee, the applicant is issued the Final No Objection Certificate (Form 28-B). This certificate, issued by the Indian International Economic Service, serves as the final and legally valid document confirming that there are no pending objections or restrictions by the Government of India on the said international funds.</p>
              <p class="hindi-text">NOC शुल्क के सफल भुगतान के पश्चात् आवेदक को अंतिम अनापत्ति प्रमाणपत्र (फॉर्म 28‑B) जारी किया जाता है। भारतीय अंतर्राष्ट्रीय आर्थिक सेवा द्वारा जारी यह प्रमाणपत्र इस बात का अंतिम और विधिसम्मत प्रमाण होता है कि संबंधित अंतर्राष्ट्रीय धन पर भारत सरकार की ओर से कोई लंबित आपत्ति या प्रतिबंध नहीं है।</p>
            </div>

            <div class="role-section role-group">
              <h3>
                <span class="english-text">Once Form 28-B is issued:</span>
                <span class="hindi-text">फॉर्म 28‑B जारी होने के बाद:</span>
              </h3>
              <ol class="numbered-list">
                <li><span class="english-text">All temporary or permanent restrictions imposed on the international funds are permanently lifted,</span><span class="hindi-text">अंतर्राष्ट्रीय धन पर लगाए गए सभी अस्थायी/स्थायी प्रतिबंध स्थायी रूप से समाप्त हो जाते हैं,</span></li>
                <li><span class="english-text">The process of withdrawal, transfer, or investment of the funds can officially begin, and The funds can be utilized lawfully in accordance with the applicable financial and statutory provisions.</span><span class="hindi-text">धन की निकासी, हस्तांतरण या निवेश की प्रक्रिया औपचारिक रूप से प्रारंभ की जा सकती है तथा लागू वित्तीय एवं वैधानिक प्रावधानों के अनुसार विधिसम्मत उपयोग किया जा सकता है।</span></li>
               
              </ol>
            </div>


            <div class="final-note">
              <h3>
                <span class="english-text">Conclusion</span>
                <span class="hindi-text">निष्कर्ष</span>
              </h3>
              <p class="english-text">
                The entire procedure aims to ensure that every international fund received in India adheres to the highest standards of financial transparency, legal compliance, and economic integrity. The Indian International Economic Service, under the supervision of the IIES, continues to work diligently to strengthen, secure, and regulate international financial transactions in alignment with the economic policies of the Government of India.
              </p>
