                    <h2>Guidelines for IIES Officials</h2>

                    <div class="preamble-section">
                        <h3>IIES Code of Conduct for Officials and Employees</h3>
                        <p>
                            <strong>Preamble:</strong><br />
                            All officials and employees working at IIES are committed to
                            upholding the reputation, efficiency, and integrity of the
                            institution. This Code of Conduct outlines the values, rules,
                            and expectations that every employee is required to follow.
                        </p>
                    </div>

                    <!-- Code of Conduct Sections -->
                    <div class="conduct-section">
                        <h3>General Code of Conduct</h3>
                        <ol class="numbered-list">
                            <li>
                                <strong>Work Ethics:</strong> All employees must be punctual
                                and dedicated to their duties. Tasks must be completed with
                                honesty, responsibility, and diligence. Follow the
                                instructions of senior officials and work in a spirit of
                                teamwork.
                            </li>
                            <li>
                                <strong>Discipline:</strong> Maintain disciplined behavior in
                                the workplace. Use of offensive language, disrespectful
                                behavior, or any form of violence is unacceptable. Negligence,
                                intentional delay, or unnecessary absenteeism is considered
                                inappropriate.
                            </li>
                            <li>
                                <strong>Confidentiality:</strong> Do not share internal
                                information, documents, or data of the institution without
                                proper authorization. Maintain the confidentiality of all
                                sensitive information.
                            </li>
                            <li>
                                <strong>Integrity and Impartiality:</strong> Maintain honesty
                                and impartiality in all tasks. Bribery, favoritism, or
                                seeking/giving undue advantage is strictly prohibited.
                            </li>
                            <li>
                                <strong>Use of Institutional Property:</strong> Institutional
                                property should be used strictly for official purposes. Any
                                kind of misuse, wastage, or damage will be the responsibility
                                of the concerned individual.
                            </li>
                            <li>
                                <strong>Gender Sensitivity and Inclusiveness:</strong> Treat
                                everyone at the workplace with respect and dignity. Gender
                                discrimination, harassment, or any antisocial behavior will
                                not be tolerated.
                            </li>
                            <li>
                                <strong>Cleanliness and Environment:</strong> Keep the
                                workplace clean and organized. Be environmentally conscious
                                and use resources sustainably.
                            </li>
                            <li>
                                <strong>Grievance Redressal:</strong> If any employee faces an
                                issue, they may file a complaint through the prescribed
                                procedure. All grievances will be addressed fairly and
                                promptly.
                            </li>
                            <li>
                                <strong>Disciplinary Action:</strong> Violation of this Code
                                of Conduct may result in disciplinary action, including
                                warnings, salary deductions, suspension, or termination of
                                service, depending on the severity of the case.
                            </li>
                        </ol>
                    </div>

                    <!-- Specific Role-Based Instructions -->
                    <h3>Specific Role-Based Instructions</h3>
                    <div class="final-note">
                        <h3>Final Note</h3>
                        <p>
                            Every member of IIES must follow this Code of Conduct and
                            contribute to maintaining the dignity and reputation of the
                            institution.
                        </p>
                    </div>
                    <div class="role-section">
                        <h3>General Code of Conduct</h3>
                        <ol class="numbered-list">
                            <li>
                                All officials must work with honesty, transparency, and
                                impartiality.
                            </li>
                            <li>
                                Any form of bias, misuse of funds, or violation of rules is
                                unacceptable.
                            </li>
                            <li>
                                All work must conform to IIES policies, Indian law, and
                                financial regulations.
                            </li>
                            <li>
                                It is mandatory to manage sensitive information within the
                                service with confidentiality and security.
                            </li>
                        </ol>
                    </div>

                    <div class="role-section role-senior">
                        <h3>Senior Officials (Director / Head)</h3>
                        <ol class="numbered-list">
                            <li>
                                Oversee the entire NOC process and make decisions according to
                                policy.
                            </li>
                            <li>
                                Ensure timely review of group (Group) and large NOC
                                applications.
                            </li>
                            <li>
                                Ensure the training and updates of departmental employees.
                            </li>
                            <li>
                                Give final approval for all NOC issuances and securely
                                maintain records.
                            </li>
                            <li>Evaluate and guide the performance of officials.</li>
                        </ol>
                    </div>

                    <div class="role-section role-mid">
                        <h3>Mid-Level Officials (Manager / Officer)</h3>
                        <ol class="numbered-list">
                            <li>
                                Conduct the initial review and document verification of NOC
                                applications.
                            </li>
                            <li>
                                Immediately inform the senior official if there are suspicious
                                or unusual documents.
                            </li>
                            <li>
                                Request additional information or documents from the
                                applicant.
                            </li>
                            <li>Check the fee payment and receipt.</li>
                            <li>
                                Ensure adherence to the timeline in the review of group NOC
                                applications.
                            </li>
                        </ol>
                    </div>

                    <div class="role-section role-junior">
                        <h3>Junior Officials / Assistants</h3>
                        <ol class="numbered-list">
                            <li>
                                Collect, classify, and digitally record application documents.
                            </li>
                            <li>
                                Conduct a preliminary check of the application form to ensure
                                all necessary documents have been submitted.
                            </li>
                            <li>
                                Provide status updates and information to the applicant.
                            </li>
                            <li>
                                Prepare necessary preliminary data and reports for NOC
                                issuance.
                            </li>
                            <li>
                                Assist in record management and make documents available for
                                audit.
                            </li>
                        </ol>
                    </div>

                    <div class="role-section role-group">
                        <h3>Group Work and Team Coordination</h3>
                        <ol class="numbered-list">
                            <li>
                                All officials must ensure teamwork and timeliness in the NOC
                                process.
                            </li>
                            <li>
                                Clearly define the responsibility of each member in a group
                                application.
                            </li>
                            <li>
                                Senior officials must periodically review and guide the team.
                            </li>
                            <li>
                                Ensure clear communication and information exchange among all
                                officials.
                            </li>
                        </ol>
                    </div>

                    <div class="role-section role-training">
                        <h3>Training and Updates</h3>
                        <ol class="numbered-list">
                            <li>
                                Officials must regularly receive training on the NOC process
                                and financial regulations.
                            </li>
                            <li>
                                Update their work procedures according to new rules,
                                notifications, and internal directives.
                            </li>
                        </ol>
