                    <div class="regulation-section">
                        <h3 data-i18n="about.about_institution.title">About the Institution</h3>
                        <p data-i18n="about.about_institution.p1">The Indian International Economic Service (IIES) is a premier Group 'A' Central Service of the Government of India, established in the year 1961 with the objective of developing professional expertise in the fields of economic policy formulation, analysis, and international financial cooperation within the government framework.</p>
                        <p data-i18n="about.about_institution.p2">The service operates across various ministries and departments of the Government of India, providing economic analysis, advisory support, and policy inputs on matters related to both domestic and international finance.</p>
                        <ul>
                            <li data-i18n="about.about_institution.li1">Foreign exchange transactions are governed under the Foreign Exchange Management Act (FEMA), 1999.</li>
                            <li data-i18n="about.about_institution.li2">IIES ensures that the source of foreign funds is legitimate and all transactions comply with FEMA rules.</li>
                            <li data-i18n="about.about_institution.li3">Compliance with RBI-prescribed limits for foreign currency, permissions for foreign investments, and NRI/PIO fund transfer regulations is mandatory.</li>
                            <li data-i18n="about.about_institution.li4">For large sums of funds, RBI requires verification and reporting.</li>
                        </ul>
                    </div>
                    <!-- objective  -->
                    <div class="regulation-section">
                        <h3 data-i18n="about.objectives.title">Objectives</h3>

                        <ul>
                            <li data-i18n="about.objectives.li1">To provide expert advice to the Government of India on issues related to international funding, investment, and financial assistance.</li>
                            <li data-i18n="about.objectives.li2">To offer analytical and policy support for development policies, economic reforms, and global cooperation programs.</li>
                            <li data-i18n="about.objectives.li3">To assist various ministries and departments in reviewing foreign funding proposals and investment plans.</li>
                            <li data-i18n="about.objectives.li4">To align India's economic strategies with global financial standards and international frameworks.</li>
                        </ul>
                    </div>
                    <!-- Special Powers (Granted in 2017) -->
                    <div class="regulation-section">
                        <h3 data-i18n="about.powers.title">Special Powers (Granted in 2017)</h3>
                        <p data-i18n="about.powers.p">In the year 2017, the Government of India conferred Special Powers upon the Indian International Economic Service (IIES), authorizing it to undertake the following functions:</p>
                        <ul>
                            <li data-i18n-html="about.powers.li1"><strong>Audit & Verification</strong> To audit, verify, and examine any type of funds or financial assistance entering India from foreign sources.</li>
                            <li data-i18n-html="about.powers.li2"><strong>Due Diligence</strong> To conduct legal, financial, and procedural compliance checks (Due Diligence) of such funds.</li>
                            <li data-i18n-html="about.powers.li3"><strong>NOC Issuance</strong> Upon completion of due process, to issue the No Objection Certificate (NOC) for the disbursement or utilization of the verified funds.</li>
                            <li data-i18n-html="about.powers.li4"><strong>Transparency & Accountability</strong> To ensure transparency, accountability, and lawful utilization of all international funds entering India.</li>
                        </ul>
                    </div>

