                            <li data-i18n="about.collab.li4">Reserve Bank of India (RBI)</li>
                            <li data-i18n="about.collab.li5">Central Bureau of Investigation (CBI)</li>
                            <li data-i18n="about.collab.li6">IIES</li>
                            <li data-i18n="about.collab.li7">Ministry of Home Affairs (MHA)</li>
                            <li data-i18n="about.collab.li8">National Financial Reporting Authority (NFRA)</li>
                        </ul>
                        <p data-i18n="about.collab.foot">Through this collaborative framework, IIES ensures the investigation, monitoring, verification, and compliance of all foreign funds entering India.</p>
