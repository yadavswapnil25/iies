                <span class="english-text">1 Introduction</span>
                <span class="hindi-text">1 परिचय</span>
              </h3>
              <p class="english-text">The Indian International Economic Service (IIES) issues a No Objection Certificate (NOC) to ensure the legitimacy of funds originating from foreign sources and to confirm regulatory compliance. These guidelines are designed for both IIES officials and NOC applicants to ensure the process is simple, transparent, and efficient.</p>
              <p class="hindi-text">भारतीय अंतर्राष्ट्रीय आर्थिक सेवा (IIES) विदेशी स्रोतों से प्राप्त धन की वैधता सुनिश्चित करने और नियामकीय अनुपालन की पुष्टि हेतु अनापत्ति प्रमाणपत्र (NOC) जारी करती है। ये दिशानिर्देश IIES अधिकारियों और NOC आवेदकों दोनों के लिए तैयार किए गए हैं ताकि प्रक्रिया सरल, पारदर्शी और कुशल रहे।</p>
            </div>

