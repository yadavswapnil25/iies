        <div class="page-main otherpage">
          <h2>4. Guidelines for IIES Officials</h2>
        </div>

        <section class="divisions-section" role="region" aria-label="Divisions">
          <div class="divisions-grid">
            <!-- Personnel Establishment Division -->
            <div class="division-card">
              <h3>4.1 Application Review</h3>
              <ul>
                <li>
                  Ensure the completeness of the application and the
                  truthfulness of the documents.
                </li>
                <li>
                  Cross-check with relevant banks or financial institutions as
                  necessary.
                </li>
                <li>
                  Pay special attention to funds received from unauthorized or
                  suspicious sources.
                </li>
              </ul>
            </div>

            <!--4.2 NOC Issuance Procedure -->
            <div class="division-card">
              <h3>4.2 NOC Issuance Procedure</h3>
              <ul>
                <li>
                  Issue the NOC only if all documents are found to be correct.
                </li>
                <li>
                  The NOC must include the clear amount, validity period, and
                  applicant's details
                </li>
                <li>
                  Payment Booklet from the Indian International Finance
                  Commission.
                </li>
                <li>
                  Safeguard the receipt and record of all NOC issuances in both
                  digital and physical forms.
                </li>
              </ul>
            </div>

            <!-- 4.3 Handling Group Applications -->
            <div class="division-card">
              <h3>4.3 Handling Group Applications</h3>
              <ul>
                <li>
                  If the NOC is applied for as a group, the review process may
                  be longer.
                </li>
                <li>Officials may allow up to 1-2 months in such cases.</li>
                <li>
                  It is necessary to complete the verification of the legitimacy
                  and documents of every member in a group application.
                </li>
              </ul>
            </div>
