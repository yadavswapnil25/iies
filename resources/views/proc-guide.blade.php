          <div class="divisions-grid">
            <!-- Personnel Establishment Division -->
            <div class="division-card">
              <h3>3.1 Eligibility</h3>
              <ul>
                <li>To standardize the process of issuing the NOC.</li>
                <li>The applicant must be the lawful owner of the funds.</li>
                <li>
                  It is mandatory to submit all relevant financial documents and
                  proof of payment.
                </li>
              </ul>
            </div>

            <!-- 3.2 Required Documents -->
            <div class="division-card">
              <h3>3.2 Required Documents</h3>
              <ul>
                <li>Application Form (in the format prescribed by IIES).</li>
                <li>
                  Proof of Source of Funds (e.g., bank statement, Letter of
                  Authority, etc.).
                </li>
                <li>
                  Payment Booklet from the Indian International Finance
                  Commission.
                </li>
                <li>Identity proof and address proof.</li>
                <li>
                  Copy of previous NOC/No Objection Certificate (if applicable).
                </li>
              </ul>
            </div>

            <!-- Additional Division 1 -->
            <div class="division-card">
              <h3>3.3 Application Process</h3>
              <ul>
                <li>
                  Online/Offline Application: The applicant must submit the NOC
                  application on the IIES portal.
                </li>
                <li>
                  Document Submission: Upload all necessary documents or submit
                  them at the office.
                </li>
                <li>Fee Payment: Pay the prescribed NOC fee.</li>
                <li>
                  Confirmation of Process: The applicant will receive a receipt
                  after successful submission.
                </li>
              </ul>
            </div>

            <div class="division-card">
              <h3>3.4 Processing Time</h3>
              <ul>
                <li>
                  Generally, the review and issuance of an NOC application takes
                  15-30 working days.
                </li>
                <li>
                  If the NOC application is a group application, the process may
                  be longer, taking up to 1 2 months.
                </li>
                <li>Fee This time may increase in complex cases.</li>
              </ul>
            </div>

            <div class="division-card">
              <h3>3.5 Applicant's Responsibility</h3>
              <ul>
                <li>Provide accurate and complete documents.</li>
                <li>
                  Concealing or providing false information about any financial
                  details is punishable.
                </li>
                <li>
                  Submit additional documents if requested by the officials.
                </li>
              </ul>
            </div>
          </div>
