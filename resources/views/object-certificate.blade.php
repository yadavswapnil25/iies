            <h2>Purpose —       </h2>
            <p>
              The NOC fee serves two main purposes: (1) revenue generation for the Central Government, and (2) a compliance filter — collecting evidence of applicant’s ability and knowledge about incoming foreign funds. The requirement to pay a prescribed fee helps deter and detect potential money-laundering, shell transactions, or cases where the applicant is unaware of funds credited to their account.
            </p>


            <!-- If fee is not deposited on time — 
 -->
            <div class="guideline-section">
              <h3>If fee is not deposited on time — </h3>

              <!-- <h4>1. Eligible Persons:</h4> -->
              <ul>
                <li>Automatic Hold / Application Pendency: If the prescribed NOC fee is not deposited within the initial submission window (e.g., 30 calendar days from application), the NOC application will be placed on hold (status: “Fee Pending”) and processing will stop.</li>
                <li>Risk Flag & Preliminary Screening: Any application where the fund amount exceeds a threshold (for example, ₹10 lakh) but the fee remains unpaid will be automatically flagged for enhanced due diligence (EDD). This flag triggers KYC re-verification, source-of-fund documentation requests (contracts, donor details, remittance advices), and a transaction history review.</li>
                <li>Notice to Applicant: A written notice (digital and/or physical) will be sent giving the applicant a final opportunity (e.g., 15 days) to pay the fee and provide missing documents. The notice must clearly state consequences of non-payment, including possible reporting.</li>
                <li>
                 Escalation & Suspicion of ML: If fee remains unpaid and document gaps persist, the case will be escalated to the designated compliance officer and, where warranted, reported to Financial Intelligence Unit — India (FIU-IND) or other competent authority as a Suspicious Transaction Report (STR) for possible money-laundering investigation.
                </li>
                <li>Temporary Freeze / Hold on NOC & Recommendation to Bank: IIES may recommend to the relevant bank to temporarily freeze or flag the concerned inward remittance account for further verification, following legal procedures and after consulting legal/advisory team.</li>
             <li>Refusal / Rejection of NOC: After full internal review and if satisfactory explanations/documents are not provided, the NOC may be formally refused. The applicant will be informed of reasons and appeal mechanism (if any).</li>
            <li>Audit Trail & Recordkeeping: All actions, notices and decisions must be recorded in the application management system for audit, compliance and future reference.</li> 
           
          <li>Penalties & Blacklisting (Policy Option): Policy can specify penalties, additional fees or temporary blacklisting of applicants/agents who deliberately avoid fee payment or provide false documentation. Any such measure must comply with applicable laws and provide procedural fairness.</li></ul>
            </div>

