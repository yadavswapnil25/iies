              <h3>
                <span class="english-text">Summary (Paragraph):</span>
                <span class="hindi-text">सारांश (अनुच्छेद):</span>
              </h3>
              <p class="english-text">The Indian International Economic Service (IIES), operating under the supervision of the IIES, has played a crucial role in managing and issuing NOCs (No Objection Certificates) for foreign funds between the years 2017 and 2025–26. During this period, IIES has issued NOCs for foreign funds amounting to approximately ₹12 lakh crore (1.2 trillion rupees). In recognition of its transparent and efficient functioning, officers of IIES have been honored by the IIES. Additionally, any illegal or suspicious funds identified during investigations were seized and handed over to the Government of India, ensuring compliance with financial regulations and contributing to national financial security.</p>
              <p class="hindi-text">भारतीय अंतर्राष्ट्रीय आर्थिक सेवा (IIES), IIES के पर्यवेक्षण में कार्य करते हुए, 2017 से 2025–26 के बीच विदेशी धन के लिए अनापत्ति प्रमाणपत्र (NOC) के प्रबंधन और निर्गमन में महत्वपूर्ण भूमिका निभाती रही है। इस अवधि में IIES ने लगभग ₹12 लाख करोड़ के विदेशी धन के लिए NOC जारी किए। पारदर्शी और कुशल कार्यप्रणाली के लिए IIES के अधिकारियों को IIES द्वारा सम्मानित किया गया। साथ ही, जांच के दौरान पाए गए किसी भी अवैध/संदिग्ध धन को जब्त कर भारत सरकार को सौंप दिया गया, जिससे वित्तीय नियमों का पालन सुनिश्चित हुआ और राष्ट्रीय वित्तीय सुरक्षा को बल मिला।</p>
