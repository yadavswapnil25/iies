                <li><span class="english-text">Countries Involved: NOCs were granted for funds originating from major countries including the United States, China, England, Russia, the UAE, Kuwait, Sri Lanka, France, and Germany, among others.</span><span class="hindi-text">शामिल देश: संयुक्त राज्य अमेरिका, चीन, इंग्लैंड, रूस, यूएई, कुवैत, श्रीलंका, फ्रांस, जर्मनी सहित प्रमुख देशों से आने वाले धन के लिए NOC जारी हुए।</span></li>
                <li><span class="english-text">Official Recognition: Officers of IIES have been honored by the IIES for their exemplary contribution in managing foreign fund compliance.</span><span class="hindi-text">आधिकारिक सम्मान: विदेशी धन अनुपालन प्रबंधन में उत्कृष्ट योगदान के लिए IIES के अधिकारियों को IIES द्वारा सम्मानित किया गया।</span></li>
                <li><span class="english-text">Action Against Illegal Funds: Any funds found to be illegitimate or non-compliant were confiscated and handed over to the government, strengthening anti-money laundering measures.</span><span class="hindi-text">अवैध धन के विरुद्ध कार्रवाई: गैरकानूनी/असंगत पाए गए धन को जब्त कर सरकार को सौंपा गया, जिससे मनी‑लॉन्ड्रिंग निरोधक उपाय मजबूत हुए।</span></li>
