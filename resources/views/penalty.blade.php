                </li>
                <li>
                  <strong>If the daily rate of 0.05% is chosen, and payment is delayed by 20 days:</strong><br> Penalty = ₹1,120 × 0.05% × 20 = ₹11.2 ≈ ₹11<br>Total Payable = ₹1,131
                </li>
                
              </ul>
