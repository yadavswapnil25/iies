           <!-- Basis for Penalty Application: -->
          <h2>Basis for Penalty Application:</h2>
          <p>
          A penalty will be imposed if the applicant fails to deposit any NOC-related fee (Base Fee and/or
          Surcharge) within the stipulated timeframe (Fee deposit window—Example: 30 days from the
          application date). The penalty will only be applied to the <strong>outstanding payable amount</strong>
          (outstanding fee + outstanding surcharge), and not on the entire fund amount—unless otherwise
          directed in the policy.
          </p>
           <!-- Section I -->
          <div class="guidelines-content">
         
            <div class="guideline-section">
              <h3>Penalty Application Options (Daily / Weekly / Monthly):</h3>
              <p>The Authority, in line with the policy, may choose the frequency at which the penalty is applied.</p>
              <p>Suggested example rates as a standard policy option are as follows (these can be modified):</p>
              <ul>
                <li>
                 <strong>Daily Penalty:</strong> 0.05% to 02% per day (of the outstanding amount)
               
                </li>
                <li>
                 <strong> Weekly Penalty:</strong> 02% to 10 % 0 per week (of the outstanding amount)
                </li>
                <li>
                  <strong>Monthly Penalty:</strong> 05% to 20% per month (of the outstanding amount)
                </li>
                
              </ul>
            </div>
          </div>
        <!-- Section I end-->

        <p>The policy must clearly specify that only one frequency will be applied (e.g., the one primarily
        chosen—daily, weekly, or monthly). The Authority may also grant a concession/grace period for
        the initial days if they so choose when implementing any new rules/regulations.</p>


          <!--Penalty Cap (Maximum Penalty):-->
          <h2>Penalty Cap (Maximum Penalty):</h2>
          <p>A maximum limit (cap) should be set on the penalty to avoid excessive burden. A suggested
          limit: Penalty should not exceed 25% of the total outstanding or 100% of the Base Fee
          (whichever is lower)—this is a policy option and can be changed based on legal advice.</p>

          <h2>Procedure Upon Penalty Application:</h2>

          <ul style="padding-left: 20px;">
            <li>
             A Compliance Notice (Reminder) will first be sent (Day 15 and Day 25).
            </li>
            <li>
              After the final deadline (Day 31+), the prescribed penalty will be automatically applied and reflected in the system.
            </li>
            <li>
             The penalty amount will be shown on the digital receipt; any previously deposited subsidy/concession will be adjusted upon payment.
            </li>
          
             <li>
             Additional action (FIU reporting, rejection, blacklisting) may be recommended for repeated non-payers.

            </li>
          </ul>
           <!-- Section II -->
