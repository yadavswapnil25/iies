            <!-- Other Considerations (Policy Options):-->
          <h2>Other Considerations (Policy Options):</h2>
          <ul style="padding-left: 20px;">
            <li>
            You may choose to keep a zero penalty (grace period) for minor delays (e.g., the first 7 days).

            </li>
            <li>
             Provision for higher penalties and additional administrative charges may be included for applicants/agents who repeatedly delay payments

            </li>
            <li>
            Seek legal advice before implementing the penalty policy and clearly inform applicants through public notification.

            </li>
           
          </ul>

          <h2 style="color: #0b4079;">New clause — Enhanced penalty for prolonged processing (English)</h2>
          <!-- 1. Provision (English):-->
          <h2>1. Provision (English):</h2>
          <p> If issuance/processing of an NOC for any financial file experiences an abnormal or prolonged
              delay — whether due to the applicant’s actions or extended compliance review — an enhanced
              penalty (higher than the standard late penalty) may be applied. Any enhanced penalty will be
              subject to the overall penalty cap set in policy.</p>

           <!-- 2. Implementation options:-->
          <h2>2. Implementation options:</h2>
          <ul style="padding-left: 20px;">
            <li>
            If processing delay > 30 additional days → increase standard penalty by <strong>+25%.</strong>
            </li>
            <li>
            If processing delay > 60 additional days → increase standard penalty by <strong>+50%.</strong>
            </li>
            <li>
            Enhanced rates apply only to the delay period beyond the prescribed fee window and must not exceed the policy cap.
            </li>
            <li>The application management system can either auto-trigger the enhanced penalty or require compliance officer approval per case.</li>
          </ul>

         <!-- 3. Reason-based differentiation:-->
          <h2>3. Reason-based differentiation:</h2>
          <ul style="padding-left: 20px;">
            <li>
            Applicant-caused delays (missing documents, non-payment) should generally attract automatic enhanced penalties.
            </li>
            <li>
           Internally caused delays (extended legal review, inter-agency consultations) may warrant partial or full waiver — such waivers must be approved and documented.
            </li>
           
          </ul>

