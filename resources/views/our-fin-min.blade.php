            <div class="regulation-section">
              <h3>
                <span class="english-text">Indian International Economic Service (IIES)</span>
                <span class="hindi-text">भारतीय अंतर्राष्ट्रीय आर्थिक सेवा (IIES)</span>
              </h3>
              <p class="english-text">The <strong>Office of the Finance Minister</strong>, under whose purview the <strong>Indian International Economic Service (IIES)</strong>operates, serves as the central authority for supervising and guiding all financial, economic, and international fund regulatory functions. This office ensures that every financial operation, including the issuance of No Objection Certificates (NOCs) for foreign funds, remains aligned with the fiscal policies and regulatory framework of the Government of India.</p>
              <p class="hindi-text"><strong>वित्त मंत्री का कार्यालय</strong>, जिसके अधीन <strong>भारतीय अंतर्राष्ट्रीय आर्थिक सेवा (IIES)</strong> कार्य करती है, सभी वित्तीय, आर्थिक तथा अंतर्राष्ट्रीय धन नियमन कार्यों के संचालन और मार्गदर्शन का केन्द्रीय प्राधिकरण है। यह कार्यालय सुनिश्चित करता है कि प्रत्येक वित्तीय प्रक्रिया, जिसमें विदेशी धन के लिए अनापत्ति प्रमाणपत्र (NOC) जारी करना शामिल है, भारत सरकार की वित्तीय नीतियों और नियामकीय ढांचे के अनुरूप रहे।</p>
              <p class="english-text">The Finance Minister’s office plays a vital role in maintaining transparency, accountability, and financial integrity all economic activities connected with international fund movements. All key directives, approvals, and policy guidelines concerning IIES originate from this office.</p>
              <p class="hindi-text">वित्त मंत्री का कार्यालय अंतर्राष्ट्रीय धन संचलन से सम्बद्ध सभी आर्थिक गतिविधियों में पारदर्शिता, जवाबदेही और वित्तीय शुचिता बनाए रखने में महत्वपूर्ण भूमिका निभाता है। IIES से सम्बंधित सभी प्रमुख निर्देश, अनुमतियाँ और नीतिगत दिशा‑निर्देश इसी कार्यालय से जारी होते हैं।</p>
            </div>


