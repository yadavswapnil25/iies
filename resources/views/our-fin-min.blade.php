                    </h3>
            
                    <ul>
                      <li><span class="english-text">Approval of major financial regulations related to international funding.</span><span class="hindi-text">अंतर्राष्ट्रीय फंडिंग से संबंधित प्रमुख वित्तीय विनियमों की स्वीकृति।</span></li>
                      <li><span class="english-text">Monitoring the overall performance of IIES in compliance with government objectives.</span><span class="hindi-text">सरकारी उद्देश्यों के अनुरूप IIES के समग्र प्रदर्शन की निगरानी।</span></li>
                      <li><span class="english-text">Ensuring that foreign fund inflows are legitimate, traceable, and used for lawful purposes.</span><span class="hindi-text">यह सुनिश्चित करना कि विदेशी धन प्रवाह वैध, अनुरेखनीय और विधिसम्मत उद्देश्यों हेतु प्रयुक्त हो।</span></li>
